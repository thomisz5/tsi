public class Bicicleta {
    private int tamanho;
    private float preco;

    public Bicicleta(int tamanho, float preco) {
        this.tamanho = tamanho;
        this.preco = preco;
    }

    public float getPreco() {
        return preco;
    }

    public int getTamanho() {
        return tamanho;
    }
}
